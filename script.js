function createOptionElements(parent, valueArr) {
  // this function creates option elements inside a parent select element
  // how many option el will be created depends on valueArr
  valueArr.map((value) => {
    const option = document.createElement("option");
    option.textContent = value;
    option.value = value;
    parent.appendChild(option);
  });
}

function createElement(
  element,
  className,
  text,
  valueAttribute,
  dataAttribute,
  dataCreditAttribute,
  dataSubAttribute,
  dataYearAttribute
) {
  const htmlEl = document.createElement(element);
  if (className) {
    htmlEl.classList.add(className);
  }
  if (text) {
    htmlEl.textContent = text;
  }
  if (valueAttribute) {
    if (valueAttribute === "Select") {
      htmlEl.disabled = true;
      htmlEl.selected = true;
    }
    htmlEl.setAttribute("value", valueAttribute);
  }
  if (dataAttribute) {
    if (valueAttribute !== "Select") {
      htmlEl.setAttribute("data-course-name", dataAttribute);
      htmlEl.setAttribute("data-year", dataYearAttribute);
      htmlEl.setAttribute("data-subject", dataSubAttribute);
      htmlEl.setAttribute("data-credit", dataCreditAttribute);
    }
  }
  return htmlEl;
}

